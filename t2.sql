CREATE TABLE ROLE (
    "ROLE_ID" INT,
    "ROLE_DESCRIPTION" VARCHAR(255),
    CONSTRAINT PK_ROLE PRIMARY KEY(ROLE_ID)
)

CREATE TABLE EMPLOYEE (
    "EMPLOYEE_ID" INT,
    "NAME" VARCHAR(50) NOT NULL,
    "SURNAME" VARCHAR(50) NOT NULL,
    "USERNAME" VARCHAR(50) NOT NULL,
    "PASSWORD" VARCHAR(50) NOT NULL,
    "HIRE_DATE" DATE NOT NULL,
    "MAIL_ADDRESS" VARCHAR(100) NOT NULL,
    "TEL_NO" VARCHAR(20) NOT NULL,
    "ROLE_ID" INT NOT NULL,
    "MANAGER_ID" INT NULL,
    "RAL" NUMBER(10,2) NOT NULL,
    "MISCONDUCT_NUM" INT DEFAULT 0 NOT NULL,
    "MISCONDUCT_DESC" VARCHAR2(255) DEFAULT NULL,
    CONSTRAINT PK_EMP PRIMARY KEY(EMPLOYEE_ID),
    CONSTRAINT FK_ROLE FOREIGN KEY ("ROLE_ID") REFERENCES ROLE("ROLE_ID"),
    CONSTRAINT FK_EMP FOREIGN KEY ("MANAGER_ID") REFERENCES EMPLOYEE("EMPLOYEE_ID") ON DELETE SET NULL,
    CONSTRAINT U_MAIL UNIQUE("MAIL_ADDRESS"),
    CONSTRAINT U_TEL UNIQUE("TEL_NO"),
    CHECK (ROLE_ID IN(1, 2, 3, 4))
)

CREATE TABLE CURRENT_ACCOUNT (
    "ACCOUNT_NO" VARCHAR(30),
    "OWNER_CODE" VARCHAR(16) NOT NULL,
    "PIN" NUMBER(5) NOT NULL,
    "BALANCE" NUMBER(15,2) NOT NULL,
    CONSTRAINT PK_CURR_ACC PRIMARY KEY("ACCOUNT_NO"),
    CONSTRAINT U_PIN UNIQUE("PIN"),
    CONSTRAINT U_OWNER UNIQUE("OWNER_CODE"),
    CONSTRAINT CHK_PIN_LENGHT CHECK (LENGTH("PIN") = 5)
)

CREATE TABLE ACCOUNT_HOLDER (
    "FISCAL_CODE" VARCHAR(16),
    "NAME" VARCHAR(255),
    "SURNAME" VARCHAR(255),
    "EMAIL_ADDRESS" VARCHAR(255) NOT NULL,
    "TEL_NO" VARCHAR(20) NOT NULL,
    "BIRTHDAY" DATE,
    "ACCOUNT_NO" VARCHAR(30),
    "EMPLOYEE_ID" INT,
    "BAD_PAYER" VARCHAR2(3) CHECK (BAD_PAYER IN ('YES', 'NO')),
    "TYPE_WORK" VARCHAR2(3) CHECK (TYPE_WORK IN ('STU', 'EPC', 'ETC', 'SEW','RET', 'UNE')),
    CONSTRAINT PK_ACC PRIMARY KEY("FISCAL_CODE"),
    CONSTRAINT U_MAIL_ACC UNIQUE("EMAIL_ADDRESS"),
    CONSTRAINT U_TEL_ACC UNIQUE("TEL_NO"),
    CONSTRAINT FK_ACC FOREIGN KEY("ACCOUNT_NO") REFERENCES CURRENT_ACCOUNT("ACCOUNT_NO") ON DELETE SET NULL,
    CONSTRAINT FK_EMP_ACC FOREIGN KEY("EMPLOYEE_ID") REFERENCES EMPLOYEE("EMPLOYEE_ID") ON DELETE SET NULL
)

CREATE TABLE TRANSACTION (
    "TRANSACTION_ID" INT,
    "TIMESTAMP" TIMESTAMP NOT NULL,
    "MOVEMENT_TYPE" VARCHAR(15),
    "AMOUNT" NUMBER(15,2),
    "ACCOUNT_NO" VARCHAR(30),
    "EMPLOYEE_ID" INT,
    CONSTRAINT PK_TRA PRIMARY KEY("TRANSACTION_ID"),
    CONSTRAINT FK_ACC_TRA FOREIGN KEY("ACCOUNT_NO") REFERENCES CURRENT_ACCOUNT("ACCOUNT_NO") ON DELETE SET NULL,
    CONSTRAINT FK_EMP_TRA FOREIGN KEY("EMPLOYEE_ID") REFERENCES EMPLOYEE("EMPLOYEE_ID") ON DELETE SET NULL,
    CONSTRAINT CHK_MOV_TYPE CHECK("MOVEMENT_TYPE" IN ('I','O'))
)



CREATE TABLE CARD (
    "CARD_NO" VARCHAR(20),
    "CARD_TYPE" VARCHAR(50),
    "PAYMENT_CIRCUIT" VARCHAR(20),
    "LIMIT" NUMBER(15,2),
    "FLAG_ACTIVE" VARCHAR(1),
    "ACCOUNT_NO" VARCHAR(30),
    "EXPIRATION_DATE" DATE,
    "CVC" VARCHAR(4),
    "PIN" VARCHAR(5),
    CONSTRAINT PK_CARD PRIMARY KEY("CARD_NO"),
    CONSTRAINT FK_ACC_CARD FOREIGN KEY("ACCOUNT_NO")  REFERENCES CURRENT_ACCOUNT("ACCOUNT_NO") ON DELETE SET NULL,
    CONSTRAINT CHK_ACTIVE CHECK("FLAG_ACTIVE" IN ('Y','N')),
    CONSTRAINT CHK_CVC_FORMAT CHECK(REGEXP_LIKE("CVC", '^\d{3,4}$')) -- solo valori numerici regex 
)

CREATE TABLE AUDIT_LOG (
    "ID_LOG" INT,
    "TIMESTAMP" TIMESTAMP NOT NULL,
    "OPERATION" VARCHAR(255) NOT NULL,
    "EMPLOYEE_ID" INT,
    CONSTRAINT PK_AUD PRIMARY KEY("ID_LOG"),
    CONSTRAINT FK_EMP_AUD FOREIGN KEY("EMPLOYEE_ID") REFERENCES EMPLOYEE("EMPLOYEE_ID") ON DELETE SET NULL)


